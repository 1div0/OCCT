
import java.awt.*;
import java.awt.event.*;

//****************************************************************************************************************
/** */
public class SimpleFrame extends Frame{
//****************************************************************************************************************

//================================================================================================================
  /** */
  public SimpleFrame(){
//================================================================================================================
    addWindowListener(new WindowAdapter(){public void windowClosing(WindowEvent theEvent){
      System.exit(1);
    }});

    setLayout(new BorderLayout());
    add(new SimpleViewer(), BorderLayout.CENTER);
    setSize(new Dimension(500,400));
    setVisible(true);
  }

//================================================================================================================
  /**
    */
  public static void main(String[] args){
//================================================================================================================
    new SimpleFrame();
  }
}
